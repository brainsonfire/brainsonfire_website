							<p>HEROES in RECOVERY is holding events across the country, seeking to inspire a sense of community wherever they can. HEROES signature event is the Heroes 6K Run and Walk – not 5K, but 6K – to create awareness of the need for treatment and to support those who are in recovery. The number six is meaningful, not only because it represents the six heroic lives woven into the word HEROES; it also underscores the significance of pushing yourself just a little bit harder and propelling yourself just a little bit further down the road to recovery. The inaugural 6K was held in 2011 on a race course in Leiper’s Fork, Tenn., during the month of September (National Recovery Month). It was sold out with 250 participants and around 100 volunteers. Since then, we have had a number of successful races across the country; in Palm Springs, Roswell Georgia, Greenville South Carolina, Coconut Creek, Florida, Louisville, Colorado and Memphis Tennessee. The amount of races — and participants — continues to grow!</p>

